#include<iostream>// iostream is header file for taking input & printing output 
// #include is the preprocessor directive used to include files 
using namespace std ;// functionalities of standard identifiers (cin , cout) is defined in this name space 
int main(){
	int number ;
	cout << " Hello world " << endl ;	// cout is used to dispaly text written in double quotation marks as output
	cout << " Enter a number : " << endl ; //semicolon marks the end of any statement 
	cin >> number ;	// cin is used to take input from user
	cout << " Your enterd number is : " << number ;
return 0; // exit status of an integer function 
}
